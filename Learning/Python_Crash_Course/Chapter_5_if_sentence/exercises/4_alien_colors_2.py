# if-else 结构

alien_color = 'green'
if alien_color == 'green':
    print("The player gained 5 points for shooting the alien.")
else:
    print("The player gained 10 points.")

# 另外一个版本
alien_color = 'red'
if alien_color == 'green':
    print("The player gained 5 points for shooting the alien.")
else:
    print("The player gained 10 points.")
